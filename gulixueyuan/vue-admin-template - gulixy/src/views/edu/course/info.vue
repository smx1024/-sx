<template>
    <div class="app-container">

        <h2 style="text-align: center;">发布新课程</h2>

        <el-steps :active="1" process-status="wait" align-center style="margin-bottom: 40px;">
            <el-step title="填写课程基本信息" />
            <el-step title="创建课程大纲" />
            <el-step title="最终发布" />
        </el-steps>

        <el-form label-width="120px">

            <el-form-item label="课程标题">
                <el-input v-model="courseInfo.title" placeholder=" 示例：机器学习项目课：从基础到搭建项目视频课程。专业名称注意大小写" />
            </el-form-item>

            <!-- 所属分类 TODO -->
            <el-form-item label="课程讲师">
                <el-select v-model="courseInfo.teacherId" placeholder="请选择">
                    <el-option v-for="teacher in teacherList" :key="teacher.id" :label="teacher.name" :value="teacher.id" />
                </el-select>
            </el-form-item>

            <!-- 课程讲师 TODO -->
            <!-- 课程讲师 -->
            <!-- <el-form-item label="课程分类">
                <el-cascader :options="subjectOneList">
                    <template slot-scope="{ node, data }">
                        <span>{{ data.title }}</span>
                        <span v-if="!node.isLeaf"> ({{ data.children.length }}) </span>
                    </template>
                </el-cascader>
            </el-form-item> -->


            <el-form-item label="课程分类">
                <!-- 当选择框的值发生改变时,courseInfo.subjectId的值也会跟着更新。 -->
                <el-select v-model="courseInfo.subjectParentId" placeholder="一级分类" @change="getTwoSubject">
                    <el-option v-for="subject in subjectOneList" :key="subject.id" :label="subject.title"
                        :value="subject.id" />
                </el-select>
                <el-select v-model="courseInfo.subjectId" placeholder="二级分类">
                    <el-option v-for="subject in subjectTwoList" :key="subject.id" :label="subject.title"
                        :value="subject.id" />
                </el-select>
            </el-form-item>

            <el-form-item label="总课时">
                <el-input-number :min="0" v-model="courseInfo.lessonNum" controls-position="right"
                    placeholder="请填写课程的总课时数" />
            </el-form-item>

            <!-- 课程简介 TODO -->
            <el-form-item label="课程简介">
                <el-input v-model="courseInfo.description" placeholder=" " />
            </el-form-item>

            <!-- 课程封面 TODO -->
            <el-form-item label="课程封面">
                <el-upload :show-file-list="false" :on-success="handleAvatarSuccess" :before-upload="beforeAvatarUpload"
                    :action="BASE_API + '/eduoss/fileoss/upload'" class="avatar-uploader">
                    <img :src="courseInfo.cover" style="width: 200px ;height: 200px;">
                </el-upload>
            </el-form-item>

            <el-form-item label="课程价格">
                <el-input-number :min="0" v-model="courseInfo.price" controls-position="right" placeholder="免费课程请设置为0元" /> 元
            </el-form-item>

            <el-form-item>
                <el-button :disabled="saveBtnDisabled" type="primary" @click="saveOrUpdate">保存并下一步</el-button>
            </el-form-item>
        </el-form>




    </div>
</template>
<script>
import course from '@/api/course';
import teacher from '@/api/teacher';
import subject from '@/api/subject';

export default {
    data() {
        return {
            saveBtnDisabled: false,	// 保存按钮是否禁用
            courseInfo: {
                title: '',
                lessonNum: 0,
                //二级分类id
                subjectId: '',
                //一级分类id
                subjectParentId: '',
                description: '',
                price: 0,
                cover: '/1.jpg',
                teacherId: ''
            },
            courseId: '',
            teacherList: [],
            subjectOneList: [],
            subjectTwoList: [],
            BASE_API: process.env.VUE_APP_BASE_API,
        }
    },
    created() {

        this.getAllTeacher();
        this.getOnesubject();
        if (this.$route.params && this.$route.params.id) {
            this.courseId = this.$route.params.id
            this.getCourseInfo();
        }
    },
    methods: {
        saveOrUpdate() {
            if (this.courseId) {
                this.update()
            } else {
                this.save()
            }
        },
        update() {
            course.updateCourseInfo(this.courseInfo).then(response => {
                this.$message({
                    type: 'success',
                    message: '修改成功'
                })
                //跳转到第二步
                this.$router.push({ path: '/course/chapter/' + this.courseId })
            }).catch(error => {
                this.$message({
                    type: 'error',
                    message: error
                })
            })
        },

        save() {
            course.addCourseInfo(this.courseInfo).then(response => {
                this.$message({
                    type: 'success',
                    message: '添加成功'
                })
                //跳转到第二步
                this.$router.push({ path: '/course/chapter/' + response.data.id })
            })

        },
        // 获取所有讲师
        getAllTeacher() {
            teacher.getAllTeacher().then(response => {
                this.teacherList = response.data.items
            })
        },
        // 获取所有一级分类
        getOnesubject() {
            subject.getAllSubject().then(response => {
                this.subjectOneList = response.data.list
            })
        },
        // 获取所有二级分类
        getTwoSubject(value) {
            this.courseInfo.subjectId = ''
            for (var i = 0; i < this.subjectOneList.length; i++) {
                if (this.subjectOneList[i].id === value) {
                    this.subjectTwoList = this.subjectOneList[i].children
                }
            }

        },

        //上传成功
        handleAvatarSuccess(res, file) {
            this.courseInfo.cover = res.data.url;
        },
        //上传之前
        beforeAvatarUpload(file) {
            // 限制图片格式
            const isJPG = file.type === 'image/jpeg'
            // 限制图片大小
            const isLt2M = file.size / 1024 / 1024 < 5
            if (!isJPG) {
                this.$message.error('上传头像图片只能是 JPG 格式!')
            }
            if (!isLt2M) {
                this.$message.error('上传头像图片大小不能超过 5MB!')
            }
            return isJPG && isLt2M
        },
        //回显数据
        getCourseInfo() {
            console.log('getCourseInfo')
            course.getCourseInfoId(this.courseId).then(response => {
                this.courseInfo = response.data.courseInfoVo
                //查询所有的分类，包含一级二级
                //查询所有分类，包含一级和二级所有
                subject.getAllSubject().then((resp) => {
                    //获取所有一级分类
                    this.subjectOneList = resp.data.list;
                    //把所有一级分类数组进行遍历
                    for (var i = 0; i < this.subjectOneList.length; i++) {
                        //获取每个一级分类
                        var oneSubject = this.subjectOneList[i];
                        //比较当前courseInfo里面的一级分类id和所有的一级分类id是否一样
                        if (this.courseInfo.subjectParentId == oneSubject.id) {
                            //获取一级分类中所有的二级分类
                            this.subjectTwoList = oneSubject.children;
                        }
                    }
                });
                //初始化所有讲师
                this.getListTeacher();

            })
        },
    }
}


</script>
  